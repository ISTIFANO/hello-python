#  Transformer une liste imbriquée avec une fonction récursive.
list2 =[2,2,4,9,8,3]
#  map 
resultat = list(map(lambda num:num*num,list2))
print(resultat)

# filter 

filter_resultat = list(filter(lambda number:number <5,list2))

print(filter_resultat)


